const GoogleTranslate = () => {
    useEffect(() => {
        const script = document.createElement("script");
        script.src =
            "//translate.google.com/translate_a/element.js?cb=googleTranslateElementInit";
        script.async = true;
        document.body.appendChild(script);

        window.googleTranslateElementInit = () => {
            new window.google.translate.TranslateElement(
                {
                    pageLanguage: "en",
                },
                "google_translate_element"
            );
        };
    }, []);

    const handleChange = (e) => {
        const language = e.target.value;
        const googleSelect = document.querySelector(".goog-te-combo");

        if (googleSelect && language) {
            googleSelect.value = language;
            googleSelect.dispatchEvent(new Event("change"));

            setTimeout(() => {
                window.location.reload();
            }, 1000); // reload after translation takes effect
        }
    };

    return (
        <div className="mt-2 w-52 sm:w-64">
            <div id="google_translate_element" style={{ display: "none" }}></div>

            <label
                htmlFor="language-selector"
                className="block text-sm font-medium text-gray-700 mb-1"
            >
                üåê Language
            </label>

            <div className="relative">
                <select
                    id="language-selector"
                    onChange={handleChange}
                    defaultValue=""
                    className="block w-full px-3 py-1.5 text-sm text-gray-800 bg-white border border-gray-300 rounded-md shadow-sm appearance-none focus:outline-none focus:ring-1 focus:ring-green-500 focus:border-green-500"
                    style={{
                        maxHeight: "40px",
                        overflowY: "auto",
                    }}
                >
                    <option value="" disabled>
                        -- Select --
                    </option>
                    {languages.map((lang) => (
                        <option key={lang.code} value={lang.code}>
                            {lang.name}
                        </option>
                    ))}
                </select>

                {/* Custom Arrow */}
                <div className="pointer-events-none absolute inset-y-0 right-2 flex items-center text-gray-400">
                    <svg
                        className="w-4 h-4"
                        fill="none"
                        stroke="currentColor"
                        viewBox="0 0 24 24"
                    >
                        <path
                            strokeLinecap="round"
                            strokeLinejoin="round"
                            strokeWidth="2"
                            d="M19 9l-7 7-7-7"
                        />
                    </svg>
                </div>
            </div>
        </div>
    );
};

export default GoogleTranslate;
